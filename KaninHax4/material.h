class material_t
{
public:
	const char* get_name()
	{
		return call_virtual_function<const char*, 0>(this);
	}
	const char* get_texture_group_name()
	{
		return call_virtual_function<const char*, 1>(this);
	}
	void* find_var(const char* var_name, bool* found, bool complain = true) //IMaterialVar*
	{
		return call_virtual_function<void*, 27>(this, var_name, found, complain);
	}
	void alpha_modulate(float _value)
	{
		call_virtual_function<void, 27>(this, _value);
	}
	void color_modulate(float r, float g, float b)
	{
		call_virtual_function<void, 28>(this, r,g,b);
	}
	void set_material_var_flag(material_var_flags_t flag, bool on)
	{
		call_virtual_function<void, 29>(this, flag, on);
	}
	bool get_material_var_flag(material_var_flags_t flag)
	{
		return call_virtual_function<bool, 30>(this, flag);
	}
};